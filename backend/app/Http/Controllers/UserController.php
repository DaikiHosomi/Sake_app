    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        if ($id != Auth::id()) {
            return abort (403);
        } else {
        $user = Auth::user();
        $user->load('sake_category');
        $sake_categories = SakeCategory::all();
        return view('users.edit', [
            'user' => $user,
            'sake_categories' => $sake_categories,
        ]);
    }
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user = User::find($id);
        $user->name = $request->name;
        $user->gender = $request->gender;
        $user->sake_category_id = $request->sake_category_id;
        $user->favorite_sake_name = $request->favorite_sake_name;
        $user->user_self_introduction = $request->user_self_introduction;
        $user->save();
        return redirect()->route('users.index');
    }

